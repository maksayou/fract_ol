#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	size_t	i;
	char	*dest;

	size = 0;
	while (s[size])
	{
		size++;
	}	
	dest = malloc((size + 1) * sizeof(char));
	if (!dest)
		return (NULL);
	i = 0;
	while (i < size)
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
